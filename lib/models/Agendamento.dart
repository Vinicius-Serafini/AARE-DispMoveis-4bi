class Agendamento {
  String nome;
  DateTime horario;
  String medico;
  String celular;
  String email;

  Agendamento(String nome, DateTime horario, String medico, String celular, String email){
    this.nome = nome;
    this.horario = horario;
    this.medico = medico;
    this.celular = celular;
    this.email = email;
  }


}